"""
ML-Enhanced Backtester Module
Filters trades using ML model predictions.
"""

import pandas as pd
import numpy as np
from typing import Optional

from config import ML_CONFIDENCE_THRESHOLD, RESULTS_DIR, PLOTS_DIR
from strategy import EMAStrategy, run_strategy
from backtester import calculate_metrics, plot_equity_curve, generate_backtest_report
